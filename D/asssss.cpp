void solve()
{
    int n;
    cin >> n;
    int a[n];
    int b[n];
    int d[n];
    int count = 0;
    for (int i = 0; i < n; i++)
        cin >> a[i];
    for (int i = 0; i < n; i++)
        cin >> b[i];
    for (int i = 0; i < n; i++)
        d[i] = b[i] - a[i];
    sort(d, d + n);
    int i = 0;
    int j = n - 1;
    while (i < j)
    {
        if (d[i] + d[j] >= 0)
        {

            i++;
            j--;
            count++;
        }
        else
            i++;
    }
    cout << count << el;
}